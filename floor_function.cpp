#include<iostream>
#include<cmath>
using namespace std;

/*
The floor() function declared in cmath library finds the largest possible integer which is less than or equal to given float number
*/

int main(){
	
	
	cout<<floor(2.1)<<endl;
	cout<<floor(84.6)<<endl;
	cout<<floor(5.6)<<endl;
	cout<<floor(4/3)<<endl;
	
	
	int n=5;
	cout<<floor(n/2)<<endl;
	cout<<floor(1+2.6)<<endl;
	cout<<floor(8.6-2.5)<<endl;
	cout<<floor(10*2.55)<<endl;
	
	
	
}
